def convert(monthStr):
    if(monthStr == 'Apr'):
        return '4'
    elif(monthStr == 'May'):
        return '5'
    elif(monthStr == 'Jun'):
        return '6'
    elif(monthStr == 'Jur'):
        return '7'
    elif(monthStr == 'Jul'):
        return '8'
    elif(monthStr == 'Sep'):
        return '9'
    elif(monthStr == 'Oct'):
        return '10'
    elif(monthStr == 'Nov'):
        return '11'
    elif(monthStr == 'Dec'):
        return '12'
    elif(monthStr == 'Jan'):
        return '1'
    elif(monthStr == 'Feb'):
        return '2'
    elif(monthStr == 'Mar'):
        return '3'
    return 0
             
    
